public class P2C_InDec {
    public static void main(String[] args) {
        //Mendeklarasikan Dan Menginisialisasikan Variabel
        int x = 8, y = 13;
        //Menampilkan Variabel
        System.out.println("X = " +x);
        System.out.println("Y = " +y);
        System.out.println("X = " + ++x);
        System.out.println("Y = " + y++);
        System.out.println("X = " + x--);
        System.out.println("Y = " + --y);
    }
}
